-- Day 2 SQL Learning

-- Table:
-- A collection of related data organized into rows and columns.

-- Column:
-- A vertical structure that stores a specific attribute of data.

-- Row:
-- A horizontal structure that represents one complete record.

-- Record:
-- Another name for a row.

-- Schema:
-- The blueprint or structure of a database.

