-- Day 3 SQL Learning

-- Key:
-- A column or set of columns used to identify records.

-- Primary Key:
-- Uniquely identifies each row in a table.
-- Must be unique.
-- Cannot contain NULL values.

-- Candidate Key:
-- A column that can uniquely identify a record.
-- A table can have multiple candidate keys.

-- Alternate Key:
-- A candidate key that is not selected as the primary key.

-- Student Table Example

-- StudentID | Email              | Name
-- 101       | varsha@gmail.com   | Varsha
-- 102       | rahul@gmail.com    | Rahul

-- Candidate Keys:
-- StudentID
-- Email

-- Primary Key:
-- StudentID

-- Alternate Key:
-- Email